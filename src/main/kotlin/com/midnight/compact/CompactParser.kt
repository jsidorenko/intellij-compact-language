package com.midnight.compact

import com.intellij.lang.ASTNode
import com.intellij.lang.PsiBuilder
import com.intellij.lang.PsiParser
import com.intellij.psi.tree.IElementType

/**
 * Minimal parser used for syntax-highlighting support: it produces a flat PSI tree
 * whose leaves are the lexer tokens. That is enough for highlighting, brace matching
 * and commenting without a full grammar.
 */
class CompactParser : PsiParser {
    override fun parse(root: IElementType, builder: PsiBuilder): ASTNode {
        val rootMarker = builder.mark()
        while (!builder.eof()) {
            builder.advanceLexer()
        }
        rootMarker.done(root)
        return builder.treeBuilt
    }
}
